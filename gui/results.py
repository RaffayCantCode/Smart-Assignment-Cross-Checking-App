"""
gui/results.py

The results dashboard. All values shown here are placeholder/fake data
generated by `_fake_result_for()` - there is no real similarity engine
yet. The shape of the returned dict is intentionally what a real
backend would produce, so wiring in a real engine later is a matter of
replacing `_fake_result_for` with an actual API/model call.
"""

import random

from PySide6.QtCore import Qt, Signal
from PySide6.QtWidgets import (
    QWidget, QVBoxLayout, QHBoxLayout, QLabel, QFrame, QPushButton,
    QScrollArea, QGridLayout, QSizePolicy
)

from styles.theme import Colors, Fonts


def _fake_result_for(payload: dict) -> dict:
    """Generate deterministic-looking fake analysis results."""
    random.seed(len(str(payload.get("files", {}))) or 1)
    score = random.randint(35, 96)

    if score >= 75:
        risk_level, risk_color = "HIGH SIMILARITY", Colors.HIGH_RISK
    elif score >= 45:
        risk_level, risk_color = "MODERATE SIMILARITY", Colors.MEDIUM_RISK
    else:
        risk_level, risk_color = "LOW SIMILARITY", Colors.LOW_RISK

    return {
        "score": score,
        "risk_level": risk_level,
        "risk_color": risk_color,
        "matching_sections": random.randint(3, 14),
        "similar_paragraphs": random.randint(2, 20),
        "processing_time": f"{random.uniform(1.2, 4.8):.1f}s",
        "confidence_score": f"{random.randint(80, 99)}%",
        "summary": (
            "Both assignments contain highly similar concepts and multiple matching "
            "sections. Manual review is recommended."
        ) if score >= 75 else (
            "Some overlapping phrasing and structure were detected, but overall the "
            "assignments appear largely independent."
        ),
    }


class InfoCard(QFrame):
    def __init__(self, title: str, value: str, parent=None):
        super().__init__(parent)
        self.setObjectName("Card")
        layout = QVBoxLayout(self)
        layout.setContentsMargins(20, 18, 20, 18)
        layout.setSpacing(6)

        value_label = QLabel(value)
        value_label.setStyleSheet(
            f"font-size: {Fonts.H2}px; font-weight: 800; color: {Colors.TEXT_PRIMARY};"
        )
        layout.addWidget(value_label)

        title_label = QLabel(title.upper())
        title_label.setStyleSheet(
            f"font-size: {Fonts.SMALL}px; font-weight: 600; letter-spacing: 1px; "
            f"color: {Colors.TEXT_MUTED};"
        )
        layout.addWidget(title_label)


class ResultsScreen(QWidget):

    restart_requested = Signal()
    new_check_requested = Signal()

    def __init__(self, parent=None):
        super().__init__(parent)

        outer = QVBoxLayout(self)
        outer.setContentsMargins(0, 0, 0, 0)

        scroll = QScrollArea()
        scroll.setWidgetResizable(True)
        outer.addWidget(scroll)

        container = QWidget()
        scroll.setWidget(container)

        root = QVBoxLayout(container)
        root.setContentsMargins(64, 40, 64, 40)
        root.setSpacing(24)
        root.setAlignment(Qt.AlignTop)

        # -- top bar ---------------------------------------------------------
        top_bar = QHBoxLayout()
        title = QLabel("Cross-Checking Results")
        title.setStyleSheet(
            f"font-size: {Fonts.H2}px; font-weight: 700; color: {Colors.TEXT_PRIMARY};"
        )
        top_bar.addWidget(title)
        top_bar.addStretch()

        new_check_button = QPushButton("New Check")
        new_check_button.setObjectName("SecondaryButton")
        new_check_button.setCursor(Qt.PointingHandCursor)
        new_check_button.clicked.connect(self.new_check_requested.emit)
        top_bar.addWidget(new_check_button)
        root.addLayout(top_bar)

        # -- similarity score hero card ----------------------------------------
        self.score_card = QFrame()
        self.score_card.setObjectName("Card")
        self.score_card.setMinimumHeight(180)
        score_layout = QVBoxLayout(self.score_card)
        score_layout.setContentsMargins(32, 28, 32, 28)
        score_layout.setAlignment(Qt.AlignCenter)
        score_layout.setSpacing(6)

        score_title = QLabel("SIMILARITY SCORE")
        score_title.setAlignment(Qt.AlignCenter)
        score_title.setStyleSheet(
            f"font-size: {Fonts.SMALL}px; font-weight: 700; letter-spacing: 2px; "
            f"color: {Colors.TEXT_MUTED};"
        )
        score_layout.addWidget(score_title)

        self.score_value_label = QLabel("--%")
        self.score_value_label.setAlignment(Qt.AlignCenter)
        self.score_value_label.setStyleSheet(
            f"font-size: 56px; font-weight: 800; color: {Colors.ACCENT};"
        )
        score_layout.addWidget(self.score_value_label)

        self.risk_label = QLabel("")
        self.risk_label.setAlignment(Qt.AlignCenter)
        self.risk_label.setStyleSheet(f"font-size: {Fonts.H3}px; font-weight: 700;")
        score_layout.addWidget(self.risk_label)

        root.addWidget(self.score_card)

        # -- info cards grid -----------------------------------------------------
        self.info_grid = QGridLayout()
        self.info_grid.setSpacing(18)
        root.addLayout(self.info_grid)

        # -- AI summary card ---------------------------------------------------
        summary_card = QFrame()
        summary_card.setObjectName("Card")
        summary_layout = QVBoxLayout(summary_card)
        summary_layout.setContentsMargins(28, 24, 28, 24)
        summary_layout.setSpacing(10)

        summary_title = QLabel("AI SUMMARY")
        summary_title.setStyleSheet(
            f"font-size: {Fonts.SMALL}px; font-weight: 700; letter-spacing: 2px; "
            f"color: {Colors.ACCENT};"
        )
        summary_layout.addWidget(summary_title)

        self.summary_text = QLabel("")
        self.summary_text.setWordWrap(True)
        self.summary_text.setStyleSheet(
            f"font-size: {Fonts.BODY + 1}px; color: {Colors.TEXT_PRIMARY}; line-height: 150%;"
        )
        summary_layout.addWidget(self.summary_text)

        root.addWidget(summary_card)

        # -- bottom actions --------------------------------------------------
        bottom_row = QHBoxLayout()
        bottom_row.addStretch()
        restart_button = QPushButton("Run Another Check")
        restart_button.setObjectName("PrimaryButton")
        restart_button.setCursor(Qt.PointingHandCursor)
        restart_button.setMinimumWidth(220)
        restart_button.clicked.connect(self.restart_requested.emit)
        bottom_row.addWidget(restart_button)
        bottom_row.addStretch()
        root.addLayout(bottom_row)

    def display_results(self, payload: dict):
        result = _fake_result_for(payload)

        self.score_value_label.setText(f"{result['score']}%")
        self.risk_label.setText(result["risk_level"])
        self.risk_label.setStyleSheet(
            f"font-size: {Fonts.H3}px; font-weight: 700; color: {result['risk_color']};"
        )
        self.summary_text.setText(result["summary"])

        # clear and rebuild info cards
        while self.info_grid.count():
            item = self.info_grid.takeAt(0)
            if item.widget():
                item.widget().deleteLater()

        cards = [
            ("Matching Sections", str(result["matching_sections"])),
            ("Similar Paragraphs", str(result["similar_paragraphs"])),
            ("Processing Time", result["processing_time"]),
            ("Confidence Score", result["confidence_score"]),
        ]
        for i, (title, value) in enumerate(cards):
            card = InfoCard(title, value)
            self.info_grid.addWidget(card, i // 4, i % 4)
